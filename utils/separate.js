function separate(area) {
    const arr = Object.keys(place);
    const parsearea = area.map((item) => {
        const reg = `(${arr.join('|')})`;
        if(item.match(reg)) {
            return {
                name: item.match(reg)[0],
                type: 1
            };
        } else {
            for(var i in place) {
                const city = Object.keys(place[i]);
                const cityReg = `(${city.join('|')})`;
                if(item.match(cityReg)) {
                    return {
                        name: item.match(cityReg)[0],
                        type: 2,
                    }
                } else {
                    return {
                        name: item,
                        type: 3
                    }
                }
            }
        }
    })
    return parseCity(parsearea);
}

function parseCity(data) {
    return data.map(item => {
        if(item.type === 2) {
            for(var i in place) {
                if(place[i].hasOwnProperty(item.name)) {
                    return i;
                } else {
                    return item.name;
                }
            }
        }
        return item.name;
    })
}

